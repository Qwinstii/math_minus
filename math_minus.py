def add(value_x, value_y):
    return int(value_x) + int(value_y)

def sub(value_x, value_y):
    return int(value_x) - int(value_y)

def mul(value_x, value_y):
    return int(value_x) * int(value_y)

def div(value_x, value_y):
    return int(value_x) / int(value_y)

def pwr(value_x, value_y):
    return int(value_x) ** int(value_y)